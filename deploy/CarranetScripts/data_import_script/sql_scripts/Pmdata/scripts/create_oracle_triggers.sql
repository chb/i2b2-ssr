CREATE or replace TRIGGER PM_PROJECT_REQUEST_INC
	BEFORE INSERT
	ON PM_PROJECT_REQUEST
	FOR EACH ROW
	BEGIN
	SELECT PM_PARAMS.NEXTVAL INTO :NEW.ID FROM DUAL;
END;
/

